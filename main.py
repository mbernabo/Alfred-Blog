@app.route('/')
def get_all_posts():
    posts = BlogPost.query.all()
    return render_template("index.html", all_posts=posts)


@app.route('/register', methods=['GET', 'POST'])
def register():
    form = RegisterForm()
    if form.validate_on_submit():
        email = form.email.data
        if db.session.execute(db.select(User).filter_by(email=email)).scalar():
            flash('El e-mail ya existe en la base de datos. Ser√° redirigido a la Login Page', category='success')
            return redirect(url_for('login'))
        password = generate_password_hash(form.password.data, salt_length=8)
        name = form.name.data
        new_user = User(
            email=email,
            password=password,
            name=name,
        )
        db.session.add(new_user)
        db.session.commit()
        return redirect(url_for('get_all_posts'))
    return render_template("register.html", form=form)


@app.route('/login', methods=['GET', 'POST'])
def login():
    form = LoginForm()
    if form.validate_on_submit():
        user = db.session.execute(db.select(User).filter_by(email=form.email.data)).scalar()
        if user and check_password_hash(user.password, form.password.data):
            login_user(user)
            return redirect(url_for('get_all_posts'))
        else:
            flash('Your credentials are wrong. Please try again.', category='danger')
            return redirect(url_for('login'))
    return render_template("login.html", form=form)


